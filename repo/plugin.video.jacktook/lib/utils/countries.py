def _check_flag(flag, country):
	if country['flag'] == flag: return True
	return False

def _return_language(country):
	return country['language']['name']

def _flag_is_minor_than(flag, country):
	if country['flag'] < flag: return True
	return False

def _binary_search(validate_func, return_func, is_minor_func, array, target, low, high):
	# array MUST be ordered
	if low > high: return None
	else:
		mid = (low + high)//2
		if validate_func(target, array[mid]):
			return return_func(array[mid])
		elif is_minor_func(target, array[mid]):
			return _binary_search(validate_func,return_func,is_minor_func,array,target,mid+1,high)
		else:
			return _binary_search(validate_func,return_func,is_minor_func,array,target,low,mid-1)

def find_language_by_unicode(flag_unicode):
	return _binary_search(_check_flag, _return_language, _flag_is_minor_than, _countries, flag_unicode, 0, len(_countries) - 1)
